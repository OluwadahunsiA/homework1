# Print integers
num1 = int(input('Enter a number of your choice: '))
num2 = int(input('Enter a second number: '))

print(f'SUM: {num1 + num2}')
print(f'PRODUCT: {num1 * num2}')
print(f'DIVISION: {num1 // num2}')
print(f'REMAINDER: {num1 % num2}')


